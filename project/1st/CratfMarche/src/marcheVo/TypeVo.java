package marcheVo;

public class TypeVo {

	private int tno;
	private String tname;
	
	public TypeVo() {
		super();
	}

	public TypeVo(int tno, String tname) {
		super();
		this.tno = tno;
		this.tname = tname;
	}

	public int getTno() {
		return tno;
	}

	public void setTno(int tno) {
		this.tno = tno;
	}

	public String getTname() {
		return tname;
	}

	public void setTname(String tname) {
		this.tname = tname;
	}
	
	
}